const Sum = (num1, num2) => {
    console.log(`${num1} + ${num2} = ${num1 + num2}`);
};

Sum(12, 12);

const Subtract = (num1, num2) => {
    console.log(`${num1} - ${num2} = ${num1 - num2}`);
};

Subtract(12, 12);

const Multiply = (num1, num2) => {
    console.log(`${num1} x ${num2} = ${num1 * num2}`);
};

Multiply(12, 12);

const Divide = (num1, num2) => {
    console.log(`${num1} / ${num2} = ${num1 / num2}`);
};

Divide(12, 12);